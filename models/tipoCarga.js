// Incluímos a Mongoose para poder interactuar con MongoDB
var mongoose = require('mongoose'),
    Schema = mongoose.Schema;

// Creamos el Schema, que vendría a ser la estructura de cada documento
var tipoCargaSchema = new Schema({
    nombre: String
});

// Compilamos el Schema
var TipoCarga = mongoose.model('TipoCarga', tipoCargaSchema);

// Creamos las acciones para interactuar con la base de datos
module.exports = {
    create: function (tipoCarga, callback) {
        var newObject = new TipoCarga(tipoCarga);
        newObject.save(function (err, response) {
            if (err) {
                callback(err, null);
            } else {
                callback(null, response);
            }
        });
    },
    read: function (callback) {
        TipoCarga.find().exec(function (err, response) {
            if (err) {
                callback(err, null);
            } else {
                callback(null, response)
            }
        });
    },
    update: function (tipoCarga, callback) {
        TipoCarga.findByIdAndUpdate(tipoCarga._id, tipoCarga, function (err, response) {
            if (err) {
                callback(err, null);
            } else {
                callback(null, response)
            }
        });
    },
    remove: function (id, callback) {
        TipoCarga.findByIdAndRemove(id, function (err, response) {
            if (err) {
                callback(err, null);
            } else {
                callback(null, response)
            }
        });
    },
    get: function (id, callback) {
        TipoCarga.findById(id, function (err, response) {
            if (err) {
                callback(err, null);
            } else {
                callback(null, response)
            }
        });
    }
};
